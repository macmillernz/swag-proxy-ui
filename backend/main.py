@app.get("/api/proxy-hosts")
def list_proxy_hosts():
    if not PROXY_CONF_DIR.exists():
        return {"hosts": [], "warning": f"Config directory not found: {PROXY_CONF_DIR}"}

    hosts = []
    seen: set[str] = set()

    for path in sorted(PROXY_CONF_DIR.iterdir()):
        parsed = parse_conf_filename(path.name)
        if not parsed:
            continue
        name, type_, enabled, is_sample = parsed
        key = f"{name}.{type_}"
        if key in seen:
            continue
        seen.add(key)
        hosts.append({"name": name, "type": type_, "enabled": enabled, "is_sample": is_sample})

    return {"hosts": hosts}


@app.post("/api/proxy-hosts", status_code=201)
def create_proxy_host(req: NewHostRequest):
    """Create a new .conf from the SWAG _template file (or built-in fallback)."""
    name = req.name.strip().lower()
    if not re.match(r'^[a-z0-9][a-z0-9-]*$', name):
        raise HTTPException(400, detail="Name must be lowercase alphanumeric + hyphens")
    if find_conf(name):
        raise HTTPException(400, detail=f"'{name}' already exists")

    template_path = PROXY_CONF_DIR / f"_template.{req.type}.conf.sample"
    if template_path.exists():
        content = template_path.read_text()
    else:
        content = FALLBACK_TEMPLATES[req.type].format(name=name)

    dest = PROXY_CONF_DIR / f"{name}.{req.type}.conf"
    dest.write_text(content)
    return {"name": name, "type": req.type, "content": content}


@app.get("/api/proxy-hosts/{name}/content")
def get_proxy_host_content(name: str):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    path, type_, enabled, is_sample = result
    content = strip_swag_header(path.read_text())
    return {"name": name, "type": type_, "enabled": enabled, "is_sample": is_sample, "content": content}


@app.put("/api/proxy-hosts/{name}/content")
def update_proxy_host_content(name: str, body: ConfigFileUpdate):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    result[0].write_text(body.content)
    return {"name": name, "message": "saved"}


@app.delete("/api/proxy-hosts/{name}", status_code=204)
def delete_proxy_host(name: str):
    """Delete the active .conf. Any .conf.sample template is left in place,
    so a host enabled from a sample reverts to that sample on delete."""
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    result[0].unlink()


@app.post("/api/proxy-hosts/{name}/enable-sample")
def enable_sample(name: str):
    """Duplicate {name}.{type}.conf.sample → {name}.{type}.conf and return its
    content for the editor. The sample template is kept."""
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    path, type_, _, is_sample = result
    if not is_sample:
        raise HTTPException(400, detail=f"'{name}' is not a sample file")
    content = path.read_text()
    dest = PROXY_CONF_DIR / f"{name}.{type_}.conf"
    dest.write_text(content)
    return {"name": name, "type": type_, "content": content}


# ── Nginx config file endpoints ───────────────────────────────────────────────

def _is_system_nginx_conf(filename: str) -> bool:
    """Files managed elsewhere — not surfaced as user 'custom' configs."""
    if filename in EDITABLE_CONF_FILES:
        return True
    for provider in AUTH_PROVIDERS:
        if filename in (f"{provider}-server.conf", f"{provider}-location.conf"):
            return True
    return False


def _nginx_conf_path(filename: str) -> Path:
    """Resolve and validate a .conf filename in /config/nginx (no traversal)."""
    if not filename or "/" in filename or "\\" in filename:
        raise HTTPException(400, detail="Invalid filename")
    if not filename.endswith(".conf"):
        raise HTTPException(400, detail="Filename must end in .conf")
    path = NGINX_CONF_DIR / filename
    if not path.resolve().is_relative_to(NGINX_CONF_DIR.resolve()):
        raise HTTPException(400, detail="Invalid filename")
    return path


@app.get("/api/nginx-configs")
def list_nginx_configs():
    """Core editable files + any user-created custom .conf files."""
    core = [
        {"filename": f, "exists": (NGINX_CONF_DIR / f).exists()}
        for f in sorted(EDITABLE_CONF_FILES)
    ]
    custom = []
    if NGINX_CONF_DIR.exists():
        for path in sorted(NGINX_CONF_DIR.glob("*.conf")):
            if path.is_file() and not _is_system_nginx_conf(path.name):
                custom.append({"filename": path.name})
    return {"core": core, "custom": custom}


@app.post("/api/nginx-configs", status_code=201)
def create_nginx_config(body: NewConfigFile):
    name = body.filename.strip()
    if not name.endswith(".conf"):
        name += ".conf"
    if _is_system_nginx_conf(name):
        raise HTTPException(400, detail=f"'{name}' is a reserved file")
    path = _nginx_conf_path(name)
    if path.exists():
        raise HTTPException(400, detail=f"'{name}' already exists")
    path.write_text("")
    return {"filename": name, "content": ""}



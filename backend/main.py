# ── File helpers ──────────────────────────────────────────────────────────────

def parse_conf_filename(fn: str) -> Optional[tuple[str, str, bool, bool]]:
    """Returns (name, type, enabled, is_sample) or None. Skips _template files."""
    for suffix, type_, enabled, is_sample in CONF_SUFFIXES:
        if fn.endswith(suffix):
            name = fn[:-len(suffix)]
            if not name or name.startswith("_template"):
                return None
            return name, type_, enabled, is_sample
    return None


def find_conf(name: str) -> Optional[tuple[Path, str, bool, bool]]:
    """Find any proxy conf for name. Returns (path, type, enabled, is_sample)."""
    for suffix, type_, enabled, is_sample in CONF_SUFFIXES:
        path = PROXY_CONF_DIR / f"{name}{suffix}"
        if path.exists():
            return path, type_, enabled, is_sample
    return None


def strip_swag_header(content: str) -> str:
    """Remove legacy ## SWAG-UI metadata lines from the top of a conf file."""
    lines = content.splitlines(keepends=True)
    while lines and any(lines[0].startswith(p) for p in _SWAG_UI_PREFIXES):
        lines.pop(0)
    if lines and lines[0].strip() == "":
        lines.pop(0)
    return "".join(lines)


# ── Proxy host endpoints ──────────────────────────────────────────────────────

@app.get("/api/proxy-hosts")
def list_proxy_hosts():
    if not PROXY_CONF_DIR.exists():
        return {"hosts": [], "warning": f"Config directory not found: {PROXY_CONF_DIR}"}

    hosts = []
    seen: set[str] = set()

    for path in sorted(PROXY_CONF_DIR.iterdir()):
        parsed = parse_conf_filename(path.name)
        if not parsed:
            continue
        name, type_, enabled, is_sample = parsed
        key = f"{name}.{type_}"
        if key in seen:
            continue
        seen.add(key)
        hosts.append({"name": name, "type": type_, "enabled": enabled, "is_sample": is_sample})

    return {"hosts": hosts}


@app.post("/api/proxy-hosts", status_code=201)
def create_proxy_host(req: NewHostRequest):
    """Create a new .conf from the SWAG _template file (or built-in fallback)."""
    name = req.name.strip().lower()
    if not re.match(r'^[a-z0-9][a-z0-9-]*$', name):
        raise HTTPException(400, detail="Name must be lowercase alphanumeric + hyphens")
    if find_conf(name):
        raise HTTPException(400, detail=f"'{name}' already exists")

    template_path = PROXY_CONF_DIR / f"_template.{req.type}.conf.sample"
    if template_path.exists():
        content = template_path.read_text()
    else:
        content = FALLBACK_TEMPLATES[req.type].format(name=name)

    dest = PROXY_CONF_DIR / f"{name}.{req.type}.conf"
    dest.write_text(content)
    return {"name": name, "type": req.type, "content": content}


@app.get("/api/proxy-hosts/{name}/content")
def get_proxy_host_content(name: str):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    path, type_, enabled, is_sample = result
    content = strip_swag_header(path.read_text())
    return {"name": name, "type": type_, "enabled": enabled, "is_sample": is_sample, "content": content}


@app.put("/api/proxy-hosts/{name}/content")
def update_proxy_host_content(name: str, body: ConfigFileUpdate):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    result[0].write_text(body.content)
    return {"name": name, "message": "saved"}


@app.delete("/api/proxy-hosts/{name}", status_code=204)
def delete_proxy_host(name: str):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    result[0].unlink()


@app.post("/api/proxy-hosts/{name}/toggle")
def toggle_proxy_host(name: str):
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    path, type_, enabled, is_sample = result
    if is_sample:
        raise HTTPException(400, detail="Use enable-sample to activate a sample file")
    new_name = f"{name}.{type_}.conf" if not enabled else f"{name}.{type_}.conf.disabled"
    path.rename(PROXY_CONF_DIR / new_name)
    return {"name": name, "enabled": not enabled}


@app.post("/api/proxy-hosts/{name}/enable-sample")
def enable_sample(name: str):
    """Copy a .conf.sample → .conf and return its content for the editor."""
    result = find_conf(name)
    if not result:
        raise HTTPException(404, detail=f"Proxy host '{name}' not found")
    path, type_, _, is_sample = result
    if not is_sample:
        raise HTTPException(400, detail=f"'{name}' is not a sample file")
    content = path.read_text()
    dest = PROXY_CONF_DIR / f"{name}.{type_}.conf"
    dest.write_text(content)
    return {"name": name, "type": type_, "content": content}



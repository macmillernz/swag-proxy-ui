def parse_conf_filename(fn: str) -> Optional[tuple[str, str, bool, bool]]:
    """Returns (name, type, enabled, is_sample) or None. Skips _template files."""
    for suffix, type_, enabled, is_sample in CONF_SUFFIXES:
        if fn.endswith(suffix):
            name = fn[:-len(suffix)]
            if not name or name.startswith("_template"):
                return None
            return name, type_, enabled, is_sample
    return None


def find_conf(name: str) -> Optional[tuple[Path, str, bool, bool]]:
    """Find any proxy conf for name. Returns (path, type, enabled, is_sample)."""
    for suffix, type_, enabled, is_sample in CONF_SUFFIXES:
        path = PROXY_CONF_DIR / f"{name}{suffix}"
        if path.exists():
            return path, type_, enabled, is_sample
    return None


def strip_swag_header(content: str) -> str:
    """Remove legacy ## SWAG-UI metadata lines from the top of a conf file."""
    lines = content.splitlines(keepends=True)
    while lines and any(lines[0].startswith(p) for p in _SWAG_UI_PREFIXES):
        lines.pop(0)
    if lines and lines[0].strip() == "":
        lines.pop(0)
    return "".join(lines)


